const axios = require("axios");
const readline = require("readline");

const API_URL = "https://www.aeropres.in/chromeapi/dawn/v1/userreward/claim";
const LOGIN_URL = "https://www.aeropres.in/chromeapi/dawn/v1/auth/login";
let sessionToken = "";

// Membuat interface input dari terminal
const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

// Fungsi untuk menampilkan teks putih
function putih(teks) {
    return `\x1b[37m${teks}\x1b[0m`;
}

// Fungsi Login dari Menu
async function login() {
    return new Promise((resolve) => {
        rl.question(putih("üìß Masukkan Email: "), (email) => {
            rl.question(putih("üîí Masukkan Password: "), async (password) => {
                console.log(putih("üîë Sedang login..."));
                try {
                    const response = await axios.post(LOGIN_URL, { username: email, password: password });
                    sessionToken = response.data.token;
                    console.log(putih("‚úÖ Login sukses!\n"));
                    resolve(true);
                } catch (error) {
                    console.log(putih("‚ùå Login gagal! Periksa kembali email/password.\n"));
                    resolve(false);
                }
            });
        });
    });
}

// Fungsi Auto Claim
async function claimReward() {
    console.log(putih("üéÅ Mencoba klaim reward..."));
    try {
        const response = await axios.post(API_URL, {}, { headers: { Authorization: `Bearer ${sessionToken}` } });
        console.log(putih(`‚úÖ Reward berhasil diklaim! Point sekarang: ${response.data.point}\n`));
    } catch (error) {
        if (error.response?.status === 401) {
            console.log(putih("‚ö†Ô∏è Session expired, silakan login ulang.\n"));
        } else {
            console.log(putih("‚ùå Gagal klaim reward, coba lagi nanti.\n"));
        }
    }
}

